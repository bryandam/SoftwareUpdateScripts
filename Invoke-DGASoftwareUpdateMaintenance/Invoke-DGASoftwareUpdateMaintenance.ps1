If ($CleanSUGs){
    Add-TextToCMLog $LogFile "Cleaning the Software Update Groups." $component 1

    #Get all the Software Update Groups.
    Try {
        $SoftwareUpdateGroups = Get-CMSoftwareUpdateGroup
